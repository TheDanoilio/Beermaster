package com.danilo.android.beermaster.game;

public class Player {

    private String name;
    private int color;







    
    public void setColor(int color) {
        this.color = color;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName(){
        return name;
    }

    public int getColor() {
        return color;
    }
}
